  function lookup(lang, key) {
    var d = translations[lang];
    if (d && d[key] != null) return d[key];
    return translations.en[key];
  }

  function t(key) {
    var v = lookup(current, key);
    return v != null ? v : key;
  }

  function applyAttr(attr, setter) {
    var nodes = document.querySelectorAll("[" + attr + "]");
    for (var i = 0; i < nodes.length; i++) {
      var v = lookup(current, nodes[i].getAttribute(attr));
      if (v != null) setter(nodes[i], v);
    }
  }

  function apply(lang) {
    if (SUPPORTED.indexOf(lang) < 0) lang = "en";
    current = lang;
    document.documentElement.lang = lang;

    applyAttr("data-i18n", function (el, v) { el.textContent = v; });
    applyAttr("data-i18n-html", function (el, v) { el.innerHTML = v; });
    applyAttr("data-i18n-ph", function (el, v) { el.setAttribute("placeholder", v); });
    applyAttr("data-i18n-aria", function (el, v) { el.setAttribute("aria-label", v); });

    var title = lookup(current, "meta.title");
    if (title) document.title = title;

    try { localStorage.setItem(STORAGE, lang); } catch (e) {}

    var sel = document.getElementById("langSelect");
    if (sel) sel.value = lang;

    document.dispatchEvent(new CustomEvent("i18n:change", { detail: { lang: lang } }));
  }

  function detect() {
    var param;
    try { param = new URLSearchParams(location.search).get("lang"); } catch (e) {}
    if (param && SUPPORTED.indexOf(param) > -1) return param;
    var saved;
    try { saved = localStorage.getItem(STORAGE); } catch (e) {}
    if (saved && SUPPORTED.indexOf(saved) > -1) return saved;
    var nav = (navigator.language || "en").slice(0, 2).toLowerCase();
    return SUPPORTED.indexOf(nav) > -1 ? nav : "en";
  }

  function init() {
    apply(detect());
    var sel = document.getElementById("langSelect");
    if (sel) sel.addEventListener("change", function () { apply(sel.value); });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }

  return { t: t, apply: apply, supported: SUPPORTED, get current() { return current; } };
